class Student 
{
	private String name;
	private double marks;
	private int age;
	
	public Student(String name,double marks,int age){
		this.name=name;
		this.marks=marks;
		this.age=age;
	}
	
	public String getName(){
		return name;}
		
	public double getMarks(){
		return marks;
	}
    public int getAge(){
		return age;}
		
	public void setName(String name){
		this.name=name;}
		
	public void setMarks(double marks){
		this.marks=marks;}
		

}